# range(stop) ------------
print('range(stop')

for n in range(5):
    print(n)
    print()


r = range(5)
for n in r:
    print(n)
    print()


# range(stop, start) ------------
print('range(stop, start')

for n in range(1, 6):
    print(n)
    print()


# range(stop, start, step) ------------
print('range(stop, start, step')

for n in range(10, 20, 2):
    print(n)
    print()


for n in range(20, 10, -2):
    print(n)